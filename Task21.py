# using the Type() function to find out the type of the following values “DS”, 2021, 17.45, True, ‘a’.

abbreviation = "DS"
year = 2021
real_number = 17.45
boolean = True
alphabet = 'a'

print(abbreviation)
print(type(abbreviation))
print(year)
print(type(year))
print(real_number)
print(type(real_number))
print(boolean)
print(type(boolean))
print(alphabet)
print(type(alphabet))

